/*
Day 1: SQL SELECT

이 파일에서는 SQL의 가장 기본이 되는 SELECT 문을 다룬다.
SELECT는 데이터베이스에서 "무엇을 보고 싶은지"를 정의하는 명령어다.

목표:
- SELECT 기본 문법 이해
- 필요한 컬럼만 조회하는 습관 기르기
- alias, 계산 컬럼, DISTINCT 사용법 익히기
*/

-- =====================================================
-- 1. 테이블 전체 조회
-- =====================================================
-- 테이블에 어떤 컬럼이 있는지 빠르게 확인할 때 사용
-- 실무에서는 SELECT * 사용을 최소화하는 것이 좋다

SELECT *
FROM users;


-- =====================================================
-- 2. 특정 컬럼만 선택해서 조회
-- =====================================================
-- 필요한 컬럼만 명시적으로 선택하는 것이 가독성과 성능 면에서 유리하다

SELECT
    user_id,
    name,
    email
FROM users;


-- =====================================================
-- 3. 컬럼에 별칭(alias) 사용하기
-- =====================================================
-- 결과 테이블을 더 읽기 쉽게 만들 수 있다
-- AS는 생략 가능하지만, 명시적으로 쓰는 것을 권장

SELECT
    user_id AS id,
    name AS user_name,
    email AS user_email
FROM users;


-- =====================================================
-- 4. 계산된 컬럼 만들기
-- =====================================================
-- SQL에서도 간단한 수치 계산이 가능하다
-- 분석용 파생 변수를 만들 때 자주 사용된다

SELECT
    product_name,
    price,
    price * 0.9 AS discounted_price
FROM products;


-- =====================================================
-- 5. DISTINCT: 중복 제거
-- =====================================================
-- 특정 컬럼에서 중복을 제거하고 고유한 값만 확인하고 싶을 때 사용

SELECT DISTINCT
    country
FROM users;


-- =====================================================
-- 6. 문자열과 숫자를 함께 출력하기
-- =====================================================
-- SELECT 문은 단순 조회뿐 아니라, 리포트용 출력에도 활용된다

SELECT
    '총 사용자 수' AS description,
    COUNT(*) AS total_users
FROM users;


-- =====================================================
-- Day 1 요약
-- =====================================================
-- 1) SELECT는 데이터를 조회하는 기본 명령어다
-- 2) SELECT * 보다 필요한 컬럼만 선택하는 것이 좋다
-- 3) alias(AS)는 결과 가독성을 높여준다
-- 4) SQL에서도 계산 컬럼을 만들 수 있다
-- 5) DISTINCT는 중복 제거에 사용된다