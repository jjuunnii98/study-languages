# 목표
# - R의 가장 기본적인 데이터 구조인 vector를 다시 구조적으로 이해한다.
# - vector의 특징(같은 타입의 값 묶음)을 익힌다.
# - numeric / character / logical vector를 생성하고 다루는 방법을 배운다.
# - indexing, logical filtering, named vector, vectorized operation을 실습한다.
#
# 한국어 설명
# Vector는 R의 핵심 데이터 구조다.
# 같은 자료형의 값들을 순서 있게 저장하며,
# R의 많은 계산은 vector를 기준으로 동작한다.
#
# 예:
# - 숫자 벡터
# - 문자 벡터
# - 논리 벡터
#
# 이 파일은 "기초 문법"을 넘어서
# vector를 데이터 구조 관점에서 이해하는 것을 목표로 한다.
# ============================================================


# ------------------------------------------------------------
# 1) Numeric Vector
# ------------------------------------------------------------
# c() 함수는 여러 값을 하나의 vector로 묶는다.
scores <- c(85, 90, 78, 92, 88)

print(scores)

# 한국어 설명
# scores는 숫자형(numeric) vector이다.


# ------------------------------------------------------------
# 2) Character Vector
# ------------------------------------------------------------
fruits <- c("apple", "banana", "orange", "grape")

print(fruits)

# 한국어 설명
# 문자열도 vector로 저장할 수 있다.


# ------------------------------------------------------------
# 3) Logical Vector
# ------------------------------------------------------------
passed <- c(TRUE, TRUE, FALSE, TRUE, TRUE)

print(passed)

# 한국어 설명
# TRUE / FALSE 값도 logical vector로 저장된다.


# ------------------------------------------------------------
# 4) Type Checking
# ------------------------------------------------------------
print(class(scores))
print(class(fruits))
print(class(passed))

# 한국어 설명
# class() 함수로 vector의 자료형을 확인할 수 있다.


# ------------------------------------------------------------
# 5) Vector Length
# ------------------------------------------------------------
print(length(scores))
print(length(fruits))

# 한국어 설명
# length()는 vector 안에 값이 몇 개 들어 있는지 알려준다.


# ------------------------------------------------------------
# 6) Indexing
# ------------------------------------------------------------
# R은 인덱스가 1부터 시작한다.
print(scores[1])   # 첫 번째 값
print(scores[3])   # 세 번째 값

print(fruits[2])   # 두 번째 과일

# 한국어 설명
# Python과 달리 R은 0이 아니라 1부터 시작한다.


# ------------------------------------------------------------
# 7) Multiple Indexing
# ------------------------------------------------------------
print(scores[c(1, 3, 5)])
print(fruits[c(2, 4)])

# 한국어 설명
# 여러 인덱스를 동시에 선택할 수 있다.


# ------------------------------------------------------------
# 8) Negative Indexing
# ------------------------------------------------------------
# 특정 위치 제외
print(scores[-1])
print(fruits[-2])

# 한국어 설명
# 음수 인덱스는 "해당 위치 제외" 의미다.


# ------------------------------------------------------------
# 9) Vector Type Coercion
# ------------------------------------------------------------
mixed_vector <- c(1, 2, "three", 4)

print(mixed_vector)
print(class(mixed_vector))

# 한국어 설명
# R vector는 같은 타입만 유지하려고 한다.
# 그래서 숫자 + 문자를 섞으면 전체가 character로 변환된다.
# 이를 type coercion이라고 한다.


# ------------------------------------------------------------
# 10) Vectorized Operations
# ------------------------------------------------------------
print(scores + 5)
print(scores * 2)

# 한국어 설명
# R은 vector 연산을 element-wise로 수행한다.
# 즉 각 원소에 대해 같은 연산을 반복 적용한다.


# ------------------------------------------------------------
# 11) Comparison Operations
# ------------------------------------------------------------
print(scores > 85)
print(scores == 90)

# 한국어 설명
# 비교 연산 결과도 logical vector로 반환된다.


# ------------------------------------------------------------
# 12) Logical Filtering
# ------------------------------------------------------------
print(scores[scores > 85])
print(fruits[nchar(fruits) > 5])

# 한국어 설명
# 조건을 만족하는 값만 선택할 수 있다.
# 데이터 분석에서 매우 자주 사용하는 패턴이다.


# ------------------------------------------------------------
# 13) Named Vector
# ------------------------------------------------------------
names(scores) <- c("Alice", "Bob", "Charlie", "David", "Emma")

print(scores)

# 한국어 설명
# vector 각 원소에 이름을 붙일 수 있다.


# ------------------------------------------------------------
# 14) Access by Name
# ------------------------------------------------------------
print(scores["Alice"])
print(scores["David"])

# 한국어 설명
# 이름을 사용해서 특정 원소에 접근 가능하다.


# ------------------------------------------------------------
# 15) Sequence Vectors
# ------------------------------------------------------------
numbers_1 <- 1:10
numbers_2 <- seq(0, 20, by = 5)

print(numbers_1)
print(numbers_2)

# 한국어 설명
# : 연산자와 seq()를 사용하면 규칙적인 숫자 vector를 쉽게 만들 수 있다.


# ------------------------------------------------------------
# 16) Repetition
# ------------------------------------------------------------
repeat_values <- rep(c("A", "B"), times = 3)

print(repeat_values)

# 한국어 설명
# rep() 함수는 값을 반복해서 vector를 만든다.


# ------------------------------------------------------------
# 17) Summary Statistics
# ------------------------------------------------------------
print(sum(scores))
print(mean(scores))
print(max(scores))
print(min(scores))

# 한국어 설명
# numeric vector는 바로 통계 요약 함수에 넣을 수 있다.


# ------------------------------------------------------------
# 18) Sorting
# ------------------------------------------------------------
print(sort(scores))
print(sort(scores, decreasing = TRUE))

# 한국어 설명
# sort() 함수로 오름차순/내림차순 정렬 가능


# ------------------------------------------------------------
# 19) Practical Example
# ------------------------------------------------------------
student_scores <- c(71, 85, 90, 66, 78, 88)

high_scores <- student_scores[student_scores >= 80]
average_score <- mean(student_scores)

print(student_scores)
print(high_scores)
print(average_score)

# 한국어 설명
# 실제 데이터 분석에서는
# 1) 조건 필터링
# 2) 평균 계산
# 3) 특정 집단 추출
# 같은 작업을 vector로 수행한다.


# ------------------------------------------------------------
# 20) Summary Output
# ------------------------------------------------------------
cat("----- Vector Data Structure Summary -----\n")
cat("Length of scores vector:", length(scores), "\n")
cat("Class of scores vector:", class(scores), "\n")
cat("Average score:", mean(scores), "\n")
cat("Values greater than 85:", scores[scores > 85], "\n")
cat("-----------------------------------------\n")