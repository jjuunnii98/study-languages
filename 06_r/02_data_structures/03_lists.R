# 목표
# - list 자료구조를 이해한다.
# - 서로 다른 타입을 하나로 묶는 방법을 익힌다.
# - list 접근 방식 ([[ ]], [ ], $)의 차이를 이해한다.
# - 실전 데이터 분석에서 list 활용 패턴을 익힌다.
#
# 한국어 설명
# list는 R에서 가장 유연한 자료구조이다.
# 서로 다른 데이터 타입 (vector, matrix, data.frame 등)을
# 하나의 객체에 저장할 수 있다.
#
# → 실제 분석에서는 모델 결과, 데이터, 메타정보를
#   하나로 묶을 때 매우 많이 사용된다.
# ============================================================


# ------------------------------------------------------------
# 1) Basic List Creation
# ------------------------------------------------------------
my_list <- list(
  numbers = c(1, 2, 3, 4),
  text = "Hello R",
  flag = TRUE
)

print(my_list)

# 한국어 설명
# list는 다양한 타입을 함께 저장할 수 있다.


# ------------------------------------------------------------
# 2) List with Mixed Structures
# ------------------------------------------------------------
complex_list <- list(
  vector_data = c(10, 20, 30),
  matrix_data = matrix(1:4, nrow = 2),
  df_data = data.frame(id = 1:2, score = c(80, 90))
)

print(complex_list)

# 한국어 설명
# vector, matrix, data.frame 등 다양한 구조를 함께 저장 가능


# ------------------------------------------------------------
# 3) Accessing Elements
# ------------------------------------------------------------
# 1. [[ ]] → 실제 값 반환
print(my_list[[1]])

# 2. $ → 이름 기반 접근
print(my_list$text)

# 3. [ ] → list 형태 유지
print(my_list[1])

# 한국어 설명
# [[ ]] → 값 자체
# [ ]   → list 유지
# $     → 이름으로 접근


# ------------------------------------------------------------
# 4) Add New Element
# ------------------------------------------------------------
my_list$new_element <- c(100, 200)

print(my_list)

# 한국어 설명
# list는 동적으로 요소를 추가할 수 있다.


# ------------------------------------------------------------
# 5) Modify Element
# ------------------------------------------------------------
my_list$text <- "Updated Text"

print(my_list)

# 한국어 설명
# 기존 요소 수정 가능


# ------------------------------------------------------------
# 6) Remove Element
# ------------------------------------------------------------
my_list$flag <- NULL

print(my_list)

# 한국어 설명
# NULL 할당 → 해당 요소 삭제


# ------------------------------------------------------------
# 7) Named List (중요)
# ------------------------------------------------------------
student <- list(
  name = "Junyeong",
  age = 28,
  scores = c(90, 85, 88)
)

print(student$name)
print(student$scores)

# 한국어 설명
# 이름을 부여하면 데이터 구조가 훨씬 명확해진다.


# ------------------------------------------------------------
# 8) Nested List
# ------------------------------------------------------------
nested_list <- list(
  user = list(
    name = "Alice",
    age = 25
  ),
  metrics = list(
    accuracy = 0.91,
    loss = 0.2
  )
)

print(nested_list$user$name)
print(nested_list$metrics$accuracy)

# 한국어 설명
# list 안에 list를 넣는 구조
# JSON 구조와 매우 유사하다


# ------------------------------------------------------------
# 9) Loop Through List
# ------------------------------------------------------------
for (item in my_list) {
  print(item)
}

# 한국어 설명
# list는 반복문으로 순회 가능


# ------------------------------------------------------------
# 10) Apply Functions to List
# ------------------------------------------------------------
num_list <- list(a = 1:3, b = 4:6, c = 7:9)

result <- lapply(num_list, sum)
print(result)

# 한국어 설명
# lapply는 list의 각 요소에 함수 적용


# ------------------------------------------------------------
# 11) sapply vs lapply
# ------------------------------------------------------------
result_s <- sapply(num_list, sum)
print(result_s)

# 한국어 설명
# sapply는 결과를 vector로 단순화


# ------------------------------------------------------------
# 12) Practical Example: Model Result Storage
# ------------------------------------------------------------
model_result <- list(
  model_name = "linear_model",
  coefficients = c(0.5, 1.2, -0.7),
  metrics = list(
    rmse = 2.3,
    r2 = 0.85
  )
)

print(model_result)

# 특정 값 접근
print(model_result$metrics$r2)

# 한국어 설명
# 모델 결과를 구조적으로 저장할 때 매우 유용


# ------------------------------------------------------------
# 13) Convert List to Data Frame
# ------------------------------------------------------------
list_df <- data.frame(
  id = 1:3,
  value = unlist(num_list)
)

print(list_df)

# 한국어 설명
# list → data.frame 변환 가능


# ------------------------------------------------------------
# 14) Check Structure
# ------------------------------------------------------------
str(nested_list)

# 한국어 설명
# str()은 list 구조를 확인할 때 매우 중요


# ------------------------------------------------------------
# 15) Summary Example
# ------------------------------------------------------------
analysis_result <- list(
  data_summary = summary(c(10, 20, 30, 40)),
  mean_value = mean(c(10, 20, 30, 40)),
  metadata = list(
    created_by = "Junyeong",
    date = Sys.Date()
  )
)

print(analysis_result)

# 한국어 설명
# 실제 분석 결과를 하나의 list로 묶는 패턴