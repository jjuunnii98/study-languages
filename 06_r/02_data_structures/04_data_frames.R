# - 데이터 조회, 필터링, 정렬, 컬럼 생성 방법을 익힌다.
# - 간단한 데이터 분석 흐름을 구현한다.
#
# 한국어 설명
# data.frame은 R에서 가장 중요한 자료구조이다.
# 각 열(column)은 서로 다른 타입을 가질 수 있으며,
# SQL의 table 또는 pandas DataFrame과 동일한 개념이다.
#
# 실제 데이터 분석, 머신러닝, 통계 분석의 대부분은
# data.frame을 기반으로 수행된다.
# ============================================================


# ------------------------------------------------------------
# 1) Create Data Frame
# ------------------------------------------------------------
df <- data.frame(
  id = 1:5,
  name = c("Alice", "Bob", "Charlie", "David", "Eva"),
  age = c(25, 30, 28, 35, 22),
  score = c(85, 90, 78, 88, 92)
)

print(df)

# 한국어 설명
# data.frame은 열(column) 단위로 데이터를 구성한다.


# ------------------------------------------------------------
# 2) Check Structure
# ------------------------------------------------------------
str(df)
summary(df)
head(df)
tail(df)

# 한국어 설명
# str()     → 구조 확인
# summary() → 통계 요약
# head()    → 앞부분
# tail()    → 뒷부분


# ------------------------------------------------------------
# 3) Access Columns
# ------------------------------------------------------------
print(df$name)
print(df[["age"]])
print(df[, "score"])

# 한국어 설명
# $      → 컬럼 접근
# [[ ]]  → 컬럼 접근 (문자열 기반)
# [, ]   → 행/열 선택


# ------------------------------------------------------------
# 4) Row Filtering
# ------------------------------------------------------------
# 나이 30 이상
df_age30 <- df[df$age >= 30, ]
print(df_age30)

# 점수 90 이상
df_high_score <- df[df$score >= 90, ]
print(df_high_score)

# 한국어 설명
# 조건 필터링은 df[조건, ] 형태로 수행


# ------------------------------------------------------------
# 5) Select Columns
# ------------------------------------------------------------
df_subset <- df[, c("name", "score")]
print(df_subset)

# 한국어 설명
# 특정 컬럼만 선택 가능


# ------------------------------------------------------------
# 6) Add New Column
# ------------------------------------------------------------
df$pass <- df$score >= 80
print(df)

# 한국어 설명
# 새로운 컬럼을 동적으로 추가 가능


# ------------------------------------------------------------
# 7) Modify Column
# ------------------------------------------------------------
df$score <- df$score + 5
print(df)

# 한국어 설명
# 기존 컬럼 값 수정 가능


# ------------------------------------------------------------
# 8) Sorting
# ------------------------------------------------------------
# 점수 기준 오름차순
df_sorted <- df[order(df$score), ]
print(df_sorted)

# 점수 기준 내림차순
df_sorted_desc <- df[order(-df$score), ]
print(df_sorted_desc)

# 한국어 설명
# order()를 사용하여 정렬


# ------------------------------------------------------------
# 9) Basic Statistics
# ------------------------------------------------------------
mean_score <- mean(df$score)
max_score <- max(df$score)
min_score <- min(df$score)

print(mean_score)
print(max_score)
print(min_score)

# 한국어 설명
# 기본 통계 계산


# ------------------------------------------------------------
# 10) Group-like Operation (Manual)
# ------------------------------------------------------------
# 나이 그룹 생성
df$age_group <- ifelse(df$age >= 30, "30+", "Under 30")

print(df)

# 그룹별 평균 점수 계산
group_mean <- aggregate(score ~ age_group, data = df, FUN = mean)
print(group_mean)

# 한국어 설명
# aggregate()는 그룹별 집계 함수


# ------------------------------------------------------------
# 11) Conditional Transformation
# ------------------------------------------------------------
df$grade <- ifelse(
  df$score >= 90, "A",
  ifelse(df$score >= 80, "B", "C")
)

print(df)

# 한국어 설명
# 조건 기반 새로운 변수 생성


# ------------------------------------------------------------
# 12) Handle Missing Values
# ------------------------------------------------------------
df$score[2] <- NA
print(df)

# NA 제거 후 평균
mean_no_na <- mean(df$score, na.rm = TRUE)
print(mean_no_na)

# 한국어 설명
# na.rm = TRUE → 결측값 제외


# ------------------------------------------------------------
# 13) Data Frame Merge
# ------------------------------------------------------------
df_extra <- data.frame(
  id = 1:5,
  city = c("Seoul", "Busan", "Incheon", "Daegu", "Daejeon")
)

merged_df <- merge(df, df_extra, by = "id")
print(merged_df)

# 한국어 설명
# merge() → SQL join과 동일 개념


# ------------------------------------------------------------
# 14) Practical Example (Mini EDA)
# ------------------------------------------------------------
cat("----- Data Summary -----\n")
cat("Total rows:", nrow(df), "\n")
cat("Average score:", mean(df$score, na.rm = TRUE), "\n")
cat("Pass rate:", mean(df$pass), "\n")
cat("------------------------\n")

# 한국어 설명
# 실제 분석에서는 이런 summary 출력이 매우 중요


# ------------------------------------------------------------
# 15) Structure Check
# ------------------------------------------------------------
str(df)