# - 숫자, 문자, 논리값을 변수에 저장하는 방법을 익힌다.
# - print()와 cat()을 사용해 변수 값을 출력하는 기초를 배운다.
#
# 한국어 설명
# 변수(variable)는 데이터를 저장하는 이름표다.
# R에서는 분석에 필요한 숫자, 문자열, 논리값 등을
# 변수에 저장하고 재사용한다.
# ============================================================


# ------------------------------------------------------------
# 1) 숫자형 변수
# ------------------------------------------------------------
# <- 는 R에서 값을 변수에 할당할 때 가장 많이 쓰는 기호다.
age <- 27
score <- 95.5

print(age)
print(score)


# ------------------------------------------------------------
# 2) 문자형 변수
# ------------------------------------------------------------
# 문자열은 따옴표(" ") 또는 작은따옴표(' ')로 감싼다.
name <- "Junyeong"
major <- "Economics"

print(name)
print(major)


# ------------------------------------------------------------
# 3) 논리형 변수
# ------------------------------------------------------------
# TRUE / FALSE 는 R의 논리값이다.
is_student <- TRUE
has_job <- FALSE

print(is_student)
print(has_job)


# ------------------------------------------------------------
# 4) 변수 이름 사용 예시
# ------------------------------------------------------------
# 변수끼리 조합해서 새로운 값을 만들 수도 있다.
birth_year <- 1998
current_year <- 2026
calculated_age <- current_year - birth_year

print(calculated_age)


# ------------------------------------------------------------
# 5) print() 사용
# ------------------------------------------------------------
# print()는 가장 기본적인 출력 함수다.
print("Hello, R!")
print(name)
print(age)


# ------------------------------------------------------------
# 6) cat() 사용
# ------------------------------------------------------------
# cat()은 여러 값을 한 줄 문장처럼 연결해서 출력할 때 편리하다.
cat("Name:", name, "\n")
cat("Age:", age, "\n")
cat("Major:", major, "\n")


# ------------------------------------------------------------
# 7) 변수의 자료형 확인
# ------------------------------------------------------------
# class() 함수로 변수의 타입을 확인할 수 있다.
print(class(age))         # numeric
print(class(name))        # character
print(class(is_student))  # logical


# ------------------------------------------------------------
# 8) 요약 출력
# ------------------------------------------------------------
cat("----- Summary -----\n")
cat("Name:", name, "\n")
cat("Birth Year:", birth_year, "\n")
cat("Calculated Age:", calculated_age, "\n")
cat("Student Status:", is_student, "\n")
cat("---------------------\n")