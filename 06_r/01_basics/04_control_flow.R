# ============================================================
# Day 4 — R Control Flow
# File: 06_r/01_basics/04_control_flow.R
#
# 목표
# - R의 제어문(control flow)을 이해한다.
# - if / else 조건문을 사용하는 방법을 배운다.
# - for / while 반복문의 기본 구조를 익힌다.
# - break / next 같은 반복 제어 키워드를 실습한다.
#
# 한국어 설명
# 제어문(control flow)은 코드의 실행 흐름을 조절하는 문법이다.
#
# 예:
# - 조건에 따라 다른 코드를 실행
# - 같은 작업을 여러 번 반복
# - 특정 조건에서 반복 중단
#
# 데이터 분석에서는 조건 분기와 반복 처리가 매우 자주 등장한다.
# ============================================================


# ------------------------------------------------------------
# 1) if 문
# ------------------------------------------------------------
# 조건이 TRUE이면 코드 실행
score <- 85

if (score >= 80) {
  print("Pass")
}

# 한국어 설명
# score가 80 이상이면 "Pass"를 출력한다.


# ------------------------------------------------------------
# 2) if ... else 문
# ------------------------------------------------------------
age <- 17

if (age >= 18) {
  print("Adult")
} else {
  print("Minor")
}

# 한국어 설명
# 조건이 TRUE면 Adult
# FALSE면 Minor 출력


# ------------------------------------------------------------
# 3) if ... else if ... else 문
# ------------------------------------------------------------
exam_score <- 92

if (exam_score >= 90) {
  grade <- "A"
} else if (exam_score >= 80) {
  grade <- "B"
} else if (exam_score >= 70) {
  grade <- "C"
} else {
  grade <- "F"
}

print(grade)

# 한국어 설명
# 점수 구간별로 학점을 다르게 부여하는 예시


# ------------------------------------------------------------
# 4) for 반복문
# ------------------------------------------------------------
# 1부터 5까지 반복
for (i in 1:5) {
  print(i)
}

# 한국어 설명
# for 문은 정해진 횟수만큼 반복할 때 사용한다.


# ------------------------------------------------------------
# 5) 벡터와 함께 for 문 사용
# ------------------------------------------------------------
students <- c("Alice", "Bob", "Charlie")

for (student in students) {
  print(paste("Hello,", student))
}

# 한국어 설명
# students 벡터의 각 원소를 하나씩 꺼내서 출력


# ------------------------------------------------------------
# 6) 누적 합계 계산
# ------------------------------------------------------------
numbers <- c(10, 20, 30, 40)
total <- 0

for (num in numbers) {
  total <- total + num
}

print(total)

# 한국어 설명
# 반복문 안에서 값을 누적하여 합계를 계산
# 이런 패턴은 데이터 처리에서 매우 자주 사용된다.


# ------------------------------------------------------------
# 7) while 반복문
# ------------------------------------------------------------
count <- 1

while (count <= 5) {
  print(count)
  count <- count + 1
}

# 한국어 설명
# while 문은 조건이 TRUE인 동안 계속 반복된다.
# for 문보다 더 유연하지만, 종료 조건을 잘못 쓰면 무한 루프가 될 수 있다.


# ------------------------------------------------------------
# 8) break 사용
# ------------------------------------------------------------
# 특정 조건에서 반복 중단
for (i in 1:10) {
  if (i == 6) {
    break
  }
  print(i)
}

# 한국어 설명
# i가 6이 되는 순간 반복문 전체를 종료
# 출력은 1 ~ 5까지만 됨


# ------------------------------------------------------------
# 9) next 사용
# ------------------------------------------------------------
# 특정 조건만 건너뛰기
for (i in 1:10) {
  if (i %% 2 == 0) {
    next
  }
  print(i)
}

# 한국어 설명
# 짝수는 건너뛰고 홀수만 출력
# next는 "이번 반복만 건너뛰고 다음 반복으로 이동"한다.


# ------------------------------------------------------------
# 10) 조건 + 반복문 조합 예시
# ------------------------------------------------------------
scores <- c(95, 82, 67, 74, 88)

for (s in scores) {
  if (s >= 80) {
    print(paste(s, ": Good"))
  } else {
    print(paste(s, ": Needs Improvement"))
  }
}

# 한국어 설명
# 반복문 안에서 조건문을 함께 사용하면
# 데이터별로 다른 메시지를 출력할 수 있다.


# ------------------------------------------------------------
# 11) 간단한 실전형 예시
# ------------------------------------------------------------
# 벡터에서 80점 이상인 학생 수 세기
scores <- c(95, 82, 67, 74, 88)
pass_count <- 0

for (s in scores) {
  if (s >= 80) {
    pass_count <- pass_count + 1
  }
}

print(pass_count)

# 한국어 설명
# 반복문 + 조건문 + 누적 계산이 결합된 패턴
# 실제 데이터 분석 로직의 매우 기본적인 형태


# ------------------------------------------------------------
# 12) 요약 출력
# ------------------------------------------------------------
cat("----- Control Flow Summary -----\n")
cat("Grade for exam_score:", grade, "\n")
cat("Total of numbers:", total, "\n")
cat("Pass count in scores:", pass_count, "\n")
cat("--------------------------------\n")