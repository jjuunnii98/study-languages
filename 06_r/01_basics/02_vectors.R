# 목표
# - R의 가장 기본적인 데이터 구조인 vector를 이해한다.
# - vector 생성, 접근, 연산, 통계 계산 방법을 배운다.
# - 논리 조건을 이용한 vector filtering 방법을 익힌다.
#
# 한국어 설명
# Vector는 R에서 가장 기본적인 데이터 구조다.
# 같은 타입의 값들을 하나의 배열처럼 묶어 저장한다.
#
# 예:
# 숫자 벡터
# 문자 벡터
# 논리 벡터
# ============================================================


# ------------------------------------------------------------
# 1) 숫자 벡터 생성
# ------------------------------------------------------------
# c() 함수는 여러 값을 하나의 vector로 묶어준다.
scores <- c(85, 90, 78, 92, 88)

print(scores)


# ------------------------------------------------------------
# 2) 문자 벡터 생성
# ------------------------------------------------------------
students <- c("Alice", "Bob", "Charlie", "David")

print(students)


# ------------------------------------------------------------
# 3) 논리 벡터
# ------------------------------------------------------------
passed <- c(TRUE, TRUE, FALSE, TRUE)

print(passed)


# ------------------------------------------------------------
# 4) 벡터 길이 확인
# ------------------------------------------------------------
# length() 함수는 vector 안에 몇 개의 값이 있는지 알려준다.
length(scores)


# ------------------------------------------------------------
# 5) 벡터 인덱싱 (indexing)
# ------------------------------------------------------------
# R의 인덱스는 1부터 시작한다.
scores[1]   # 첫 번째 값
scores[3]   # 세 번째 값

students[2]


# ------------------------------------------------------------
# 6) 여러 값 선택
# ------------------------------------------------------------
scores[c(1,3,5)]

students[c(2,4)]


# ------------------------------------------------------------
# 7) 벡터 연산
# ------------------------------------------------------------
# R은 vector 연산을 자동으로 element-wise로 수행한다.
scores + 5

scores * 2


# ------------------------------------------------------------
# 8) 통계 계산
# ------------------------------------------------------------
mean(scores)   # 평균
sum(scores)    # 합계
max(scores)    # 최대값
min(scores)    # 최소값


# ------------------------------------------------------------
# 9) 논리 조건 벡터
# ------------------------------------------------------------
# 특정 조건을 만족하는지 확인
scores > 85


# ------------------------------------------------------------
# 10) 조건 기반 필터링
# ------------------------------------------------------------
# 조건에 맞는 값만 선택할 수 있다.
scores[scores > 85]


# ------------------------------------------------------------
# 11) 벡터 이름 지정
# ------------------------------------------------------------
# names()를 사용하면 각 값에 이름을 붙일 수 있다.
names(scores) <- c("Alice", "Bob", "Charlie", "David", "Emma")

print(scores)


# ------------------------------------------------------------
# 12) 이름으로 값 접근
# ------------------------------------------------------------
scores["Alice"]
scores["Charlie"]


# ------------------------------------------------------------
# 13) 시퀀스 생성
# ------------------------------------------------------------
# : 연산자는 숫자 시퀀스를 만든다.
numbers <- 1:10
print(numbers)


# ------------------------------------------------------------
# 14) seq() 함수
# ------------------------------------------------------------
# seq()는 간격을 지정할 수 있다.
seq(0, 10, by = 2)


# ------------------------------------------------------------
# 15) 요약 출력
# ------------------------------------------------------------
cat("----- Vector Summary -----\n")
cat("Number of scores:", length(scores), "\n")
cat("Average score:", mean(scores), "\n")
cat("Highest score:", max(scores), "\n")
cat("Lowest score:", min(scores), "\n")
cat("--------------------------\n")