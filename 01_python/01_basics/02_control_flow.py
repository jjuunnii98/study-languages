"""
Day 2: Python Control Flow

이 파일에서는 파이썬의 제어 흐름(control flow)을 다룬다.
- 조건문 (if / elif / else)
- 반복문 (for / while)
- 반복 제어 (break / continue)
"""

# ==================================================
# 1. 조건문 (if / elif / else)
# ==================================================
# 조건에 따라 다른 코드 블록을 실행한다.

age = 20

if age >= 20:
    print("성인입니다.")
elif age >= 17:
    print("고등학생입니다.")
else:
    print("미성년자입니다.")

# 👉 if는 항상 첫 조건
# 👉 elif는 추가 조건
# 👉 else는 위 조건이 모두 거짓일 때 실행


# ==================================================
# 2. 비교 연산자와 논리 연산자
# ==================================================
# 비교 연산자: >, <, >=, <=, ==, !=
# 논리 연산자: and, or, not

score = 85

if score >= 80 and score < 90:
    print("등급: B")

if score >= 90 or score == 100:
    print("우수한 성적입니다.")

if not score < 60:
    print("불합격은 아닙니다.")


# ==================================================
# 3. for 반복문
# ==================================================
# for문은 정해진 횟수 또는 iterable 객체를 순회한다.

# range(start, end)
for i in range(1, 6):
    print("i =", i)

# 리스트 순회
languages = ["Python", "SQL", "JavaScript"]

for lang in languages:
    print("학습 언어:", lang)


# ==================================================
# 4. while 반복문
# ==================================================
# 조건이 True인 동안 반복 실행된다.

count = 0

while count < 3:
    print("count =", count)
    count += 1

# ⚠️ while문은 조건을 잘못 작성하면 무한 루프가 될 수 있음


# ==================================================
# 5. break 와 continue
# ==================================================
# break: 반복문 즉시 종료
# continue: 현재 반복을 건너뛰고 다음 반복으로 이동

for i in range(1, 6):
    if i == 3:
        continue  # 3은 출력하지 않음
    print("continue 예제:", i)

for i in range(1, 6):
    if i == 4:
        break  # 4에서 반복 종료
    print("break 예제:", i)


# ==================================================
# 6. 실전 예제: 합격 여부 판단
# ==================================================
# 점수가 60점 이상이면 합격

scores = [45, 67, 82, 59, 90]

for s in scores:
    if s >= 60:
        print(s, ": 합격")
    else:
        print(s, ": 불합격")


"""
Day 2 요약
- if/elif/else로 조건 분기 가능
- for/while로 반복 처리
- break/continue로 반복 흐름 제어
- 제어문은 모든 알고리즘과 데이터 처리의 기본
"""