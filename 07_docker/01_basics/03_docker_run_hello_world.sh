#!/bin/bash

# ============================================================
# Day 3 — docker run hello-world
# File: 07_docker/01_basics/03_docker_run_hello_world.sh
#
# 목표
# - Docker의 가장 기본적인 실행 명령어를 이해한다.
# - hello-world 이미지를 실행하여 Docker가 정상 동작하는지 확인한다.
# - image, container, run의 관계를 감각적으로 익힌다.
#
# 한국어 설명
# docker run hello-world 는 Docker 입문에서 가장 많이 사용하는
# 첫 테스트 명령어이다.
#
# 이 명령어를 실행하면:
# 1) hello-world 이미지가 로컬에 있는지 확인
# 2) 없으면 Docker Hub에서 다운로드
# 3) 컨테이너 실행
# 4) 성공 메시지 출력
# ============================================================


# ------------------------------------------------------------
# 1) Docker 버전 확인
# ------------------------------------------------------------
# Docker 명령어가 정상적으로 인식되는지 먼저 확인한다.
echo "Checking Docker version..."
docker --version

echo "----------------------------------------"


# ------------------------------------------------------------
# 2) hello-world 이미지 실행
# ------------------------------------------------------------
# 가장 기본적인 Docker 테스트
#
# 한국어 설명
# - hello-world 이미지는 Docker 공식 테스트 이미지
# - 처음 실행하면 이미지가 다운로드될 수 있음
# - 컨테이너가 메시지를 출력한 뒤 종료됨
echo "Running hello-world container..."
docker run hello-world

echo "----------------------------------------"


# ------------------------------------------------------------
# 3) 로컬 이미지 목록 확인
# ------------------------------------------------------------
# hello-world 이미지가 로컬에 저장되었는지 확인
echo "Checking local Docker images..."
docker images

echo "----------------------------------------"


# ------------------------------------------------------------
# 4) 전체 컨테이너 목록 확인
# ------------------------------------------------------------
# hello-world 컨테이너는 실행 후 바로 종료되므로
# docker ps 가 아니라 docker ps -a 로 보는 것이 좋다.
echo "Checking all containers..."
docker ps -a

echo "----------------------------------------"


# ------------------------------------------------------------
# 5) 정리 메시지
# ------------------------------------------------------------
echo "Docker hello-world practice completed."
echo "If you saw 'Hello from Docker!', your Docker installation is working correctly."