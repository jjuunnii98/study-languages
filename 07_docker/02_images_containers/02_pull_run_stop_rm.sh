#!/bin/bash

# ============================================================
# Day 6 — Docker pull / run / stop / rm
# File: 07_docker/02_images_containers/02_pull_run_stop_rm.sh
#
# 목표
# - Docker 이미지 다운로드(pull) 방법을 익힌다.
# - 이미지를 사용해 컨테이너를 실행(run)한다.
# - 실행 중인 컨테이너를 중지(stop)한다.
# - 중지된 컨테이너를 삭제(rm)한다.
#
# 한국어 설명
# 이 스크립트는 Docker의 가장 기본적인 컨테이너 lifecycle을
# 한 번에 실습하기 위한 예제다.
#
# 흐름:
# 1) nginx 이미지 다운로드
# 2) nginx 컨테이너 실행
# 3) 실행 상태 확인
# 4) 컨테이너 중지
# 5) 중지 상태 확인
# 6) 컨테이너 삭제
# 7) 최종 상태 확인
#
# 주의
# - 이 스크립트는 "docker_day6_nginx"라는 이름의 컨테이너를 사용한다.
# - 같은 이름의 컨테이너가 이미 있으면 먼저 삭제하거나 이름을 바꿔야 한다.
# ============================================================


# ------------------------------------------------------------
# 0) 변수 정의
# ------------------------------------------------------------
IMAGE_NAME="nginx:latest"
CONTAINER_NAME="docker_day6_nginx"

echo "========================================"
echo "Docker Day 6 Practice Started"
echo "Image     : ${IMAGE_NAME}"
echo "Container : ${CONTAINER_NAME}"
echo "========================================"


# ------------------------------------------------------------
# 1) Docker 버전 확인
# ------------------------------------------------------------
# 한국어 설명
# Docker 명령어가 정상 동작하는지 먼저 확인한다.
echo
echo "[1] Checking Docker version..."
docker --version


# ------------------------------------------------------------
# 2) 이미지 다운로드
# ------------------------------------------------------------
# 한국어 설명
# docker pull은 Docker Hub에서 이미지를 다운로드한다.
echo
echo "[2] Pulling image..."
docker pull "${IMAGE_NAME}"


# ------------------------------------------------------------
# 3) 로컬 이미지 확인
# ------------------------------------------------------------
# 한국어 설명
# pull 후 로컬에 이미지가 저장되었는지 확인한다.
echo
echo "[3] Listing local images..."
docker images


# ------------------------------------------------------------
# 4) 기존 같은 이름의 컨테이너가 있는지 확인
# ------------------------------------------------------------
# 한국어 설명
# 같은 이름의 컨테이너가 이미 있으면 run이 실패할 수 있다.
# 따라서 실습 안정성을 위해 기존 컨테이너를 제거한다.
echo
echo "[4] Removing existing container if it already exists..."
docker rm -f "${CONTAINER_NAME}" >/dev/null 2>&1 || true


# ------------------------------------------------------------
# 5) 컨테이너 실행
# ------------------------------------------------------------
# 옵션 설명
# -d            : detached mode (백그라운드 실행)
# --name        : 컨테이너 이름 지정
# -p 8080:80    : 내 컴퓨터의 8080 포트를 컨테이너의 80 포트와 연결
#
# 한국어 설명
# nginx 웹서버를 백그라운드에서 실행한다.
echo
echo "[5] Running container..."
docker run -d --name "${CONTAINER_NAME}" -p 8080:80 "${IMAGE_NAME}"


# ------------------------------------------------------------
# 6) 실행 중인 컨테이너 확인
# ------------------------------------------------------------
# 한국어 설명
# docker ps는 현재 실행 중인 컨테이너만 보여준다.
echo
echo "[6] Checking running containers..."
docker ps


# ------------------------------------------------------------
# 7) 전체 컨테이너 확인
# ------------------------------------------------------------
# 한국어 설명
# docker ps -a는 실행 중/중지된 컨테이너를 모두 보여준다.
echo
echo "[7] Checking all containers..."
docker ps -a


# ------------------------------------------------------------
# 8) 브라우저 안내 메시지
# ------------------------------------------------------------
# 한국어 설명
# nginx는 웹서버이므로 localhost:8080 에 접속하면 페이지가 뜬다.
echo
echo "[8] If Docker is working correctly, open this in your browser:"
echo "    http://localhost:8080"


# ------------------------------------------------------------
# 9) 컨테이너 중지
# ------------------------------------------------------------
# 한국어 설명
# docker stop은 실행 중인 컨테이너를 정상적으로 중지한다.
echo
echo "[9] Stopping container..."
docker stop "${CONTAINER_NAME}"


# ------------------------------------------------------------
# 10) 중지 후 상태 확인
# ------------------------------------------------------------
# 한국어 설명
# stop 이후에는 docker ps에는 안 보일 수 있고,
# docker ps -a에서는 Exited 상태로 확인할 수 있다.
echo
echo "[10] Checking all containers after stop..."
docker ps -a


# ------------------------------------------------------------
# 11) 컨테이너 삭제
# ------------------------------------------------------------
# 한국어 설명
# docker rm은 중지된 컨테이너를 삭제한다.
echo
echo "[11] Removing container..."
docker rm "${CONTAINER_NAME}"


# ------------------------------------------------------------
# 12) 최종 상태 확인
# ------------------------------------------------------------
# 한국어 설명
# 컨테이너 삭제 후 더 이상 목록에 없어야 정상이다.
echo
echo "[12] Final container list..."
docker ps -a


# ------------------------------------------------------------
# 13) 정리 메시지
# ------------------------------------------------------------
echo
echo "========================================"
echo "Docker Day 6 Practice Completed"
echo
echo "What you practiced:"
echo "- docker pull"
echo "- docker run"
echo "- docker stop"
echo "- docker rm"
echo
echo "Key concept:"
echo "Image -> Container -> Stop -> Remove"
echo "========================================"