#!/bin/bash

# ============================================================
# Day 3 — Shell Arguments
# File: 04_shell/01_basics/03_arguments.sh
#
# 목표
# - Shell 스크립트에 전달되는 인자(arguments)를 이해한다.
# - $0, $1, $2, $#, $@ 의 의미를 익힌다.
# - 인자 개수 검증과 usage 메시지 작성 방법을 배운다.
#
# 한국어 설명
# Shell 스크립트는 실행할 때 뒤에 값을 붙여 전달할 수 있다.
#
# 예:
# ./03_arguments.sh Alice 25
#
# 이 경우:
# $0 = 스크립트 이름
# $1 = Alice
# $2 = 25
#
# 인자는 자동화 스크립트에서 매우 중요하다.
# 예를 들어:
# - 파일 이름 받기
# - 사용자 이름 받기
# - 환경(dev/prod) 받기
# - 옵션값 받기
# ============================================================


# ------------------------------------------------------------
# 1) 기본 인자 출력
# ------------------------------------------------------------
echo "===== Basic Arguments ====="
echo "Script name (\$0): $0"
echo "First argument (\$1): $1"
echo "Second argument (\$2): $2"
echo "Number of arguments (\$#): $#"
echo "All arguments (\$@): $@"
echo "==========================="
echo

# 한국어 설명
# $0  : 현재 실행한 스크립트 이름
# $1  : 첫 번째 인자
# $2  : 두 번째 인자
# $#  : 인자 개수
# $@  : 전체 인자 목록


# ------------------------------------------------------------
# 2) 인자 개수 확인
# ------------------------------------------------------------
# 한국어 설명
# 스크립트가 기대하는 인자 개수와 실제 전달된 개수가 다른 경우
# 에러 메시지를 보여주는 패턴은 실무에서 매우 중요하다.
if [ $# -lt 2 ]; then
  echo "[ERROR] At least 2 arguments are required."
  echo "Usage: $0 <name> <age>"
  echo "Example: $0 Junyeong 27"
  echo
else
  echo "[INFO] Enough arguments were provided."
  echo "Name: $1"
  echo "Age: $2"
  echo
fi


# ------------------------------------------------------------
# 3) 모든 인자 순회
# ------------------------------------------------------------
echo "===== Loop Through All Arguments ====="

# 한국어 설명
# "$@" 는 각 인자를 안전하게 하나씩 순회할 때 사용한다.
# 공백이 포함된 인자도 더 안전하게 처리할 수 있다.
index=1
for arg in "$@"; do
  echo "Argument $index: $arg"
  index=$((index + 1))
done

echo "======================================"
echo


# ------------------------------------------------------------
# 4) 이름과 나이를 이용한 간단한 메시지 생성
# ------------------------------------------------------------
# 한국어 설명
# 첫 번째 인자 = 이름
# 두 번째 인자 = 나이
# 라고 가정한 실전형 예시
if [ $# -ge 2 ]; then
  name="$1"
  age="$2"

  echo "===== Personalized Output ====="
  echo "Hello, $name!"
  echo "You are $age years old."
  echo "This shell script received your information through command-line arguments."
  echo "================================"
  echo
fi


# ------------------------------------------------------------
# 5) 선택적 세 번째 인자(optional argument)
# ------------------------------------------------------------
# 한국어 설명
# 세 번째 인자가 들어오면 role로 사용
# 없으면 기본값을 설정하는 패턴
role="${3:-guest}"

echo "===== Optional Argument Example ====="
echo "Role: $role"
echo "====================================="
echo

# 한국어 설명
# ${3:-guest}
# → 세 번째 인자가 있으면 그 값 사용
# → 없으면 guest 사용


# ------------------------------------------------------------
# 6) 요약 출력
# ------------------------------------------------------------
echo "===== Summary ====="
echo "Script: $0"
echo "Argument count: $#"
echo "Arguments: $@"
echo "Default role handling: ${3:-guest}"
echo "==================="