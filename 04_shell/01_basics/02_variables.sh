#!/bin/bash

# ============================================================
# Day 2 — Shell Variables
# File: 04_shell/01_basics/02_variables.sh
#
# 목표
# - Shell에서 변수를 선언하고 사용하는 방법을 이해한다.
# - 사용자 정의 변수와 환경 변수를 구분한다.
# - echo와 함께 변수 값을 출력하는 기본 패턴을 익힌다.
#
# 한국어 설명
# 변수(variable)는 값을 저장하는 이름표다.
# Shell에서는 문자열, 경로, 사용자명, 파일명 같은 값을
# 변수에 저장한 뒤 재사용할 수 있다.
# ============================================================


# ------------------------------------------------------------
# 1) 사용자 정의 변수 선언
# ------------------------------------------------------------
# Shell에서는 변수 선언 시 공백 없이 작성해야 한다.
# 올바른 예:
# name="Junyeong"
#
# 잘못된 예:
# name = "Junyeong"
# ↑ 공백이 있으면 에러 발생
name="Junyeong"
role="Data Analyst"
project="study-languages"

echo "Name: $name"
echo "Role: $role"
echo "Project: $project"


# ------------------------------------------------------------
# 2) 변수 출력
# ------------------------------------------------------------
# 변수 값을 사용할 때는 앞에 $를 붙인다.
echo "Hello, $name!"
echo "$name is working on $project."


# ------------------------------------------------------------
# 3) 숫자처럼 보이는 값도 기본적으로 문자열
# ------------------------------------------------------------
# Shell 변수는 기본적으로 문자열처럼 다뤄진다.
age=27
echo "Age: $age"


# ------------------------------------------------------------
# 4) 환경 변수(Environment Variables)
# ------------------------------------------------------------
# 환경 변수는 시스템이나 Shell이 기본 제공하는 변수다.
# 예:
# USER  -> 현재 사용자
# HOME  -> 홈 디렉토리
# SHELL -> 현재 기본 Shell
echo "Current user: $USER"
echo "Home directory: $HOME"
echo "Default shell: $SHELL"


# ------------------------------------------------------------
# 5) 경로 변수 예시
# ------------------------------------------------------------
# 파일 경로나 디렉토리를 변수에 담아두면 재사용하기 쉽다.
workspace="$HOME/Desktop/Projects/public/study-languages"
echo "Workspace path: $workspace"


# ------------------------------------------------------------
# 6) 문자열 조합
# ------------------------------------------------------------
# 여러 변수를 하나의 문장 안에서 조합할 수 있다.
message="$name is preparing a portfolio for AI/Data roles."
echo "$message"


# ------------------------------------------------------------
# 7) 중괄호 사용
# ------------------------------------------------------------
# 변수 이름이 다른 문자와 붙어 헷갈릴 수 있을 때는
# ${변수명} 형태를 사용하면 안전하다.
language="Shell"
echo "Learning ${language}Basics is fun."      # 의도치 않은 변수명 해석 가능성
echo "Learning ${language} Basics is fun."     # 안전하고 권장되는 방식


# ------------------------------------------------------------
# 8) readonly 변수
# ------------------------------------------------------------
# readonly를 사용하면 값을 바꿀 수 없는 변수를 만들 수 있다.
readonly course="Shell Basics"
echo "Course: $course"

# 아래 줄은 실행 시 에러를 발생시킬 수 있으므로 주석 처리
# course="Advanced Shell"


# ------------------------------------------------------------
# 9) export 변수
# ------------------------------------------------------------
# export를 사용하면 자식 프로세스에서도 사용할 수 있는
# 환경 변수처럼 전달할 수 있다.
export APP_ENV="development"
echo "APP_ENV: $APP_ENV"


# ------------------------------------------------------------
# 10) 정리 출력
# ------------------------------------------------------------
echo "------------------------------"
echo "Variables practice completed."
echo "User: $USER"
echo "Project: $project"
echo "Environment: $APP_ENV"
echo "------------------------------"