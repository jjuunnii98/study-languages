#!/bin/bash

# ============================================================
# Day 1 — echo basics
# File: 04_shell/01_basics/01_echo.sh
#
# 목표
# - Shell script의 가장 기본 구조를 이해한다.
# - echo 명령어로 문자열을 출력하는 방법을 배운다.
# - 변수와 함께 echo를 사용하는 기초 패턴을 익힌다.
#
# 한국어 설명
# echo는 "화면에 문자열을 출력하는 명령어"이다.
# Shell 입문에서 가장 먼저 배우는 명령어 중 하나다.
# ============================================================


# ------------------------------------------------------------
# 1) 가장 기본적인 출력
# ------------------------------------------------------------
# 문자열을 그대로 터미널에 출력한다.
echo "Hello, Shell!"


# ------------------------------------------------------------
# 2) 여러 문장 출력
# ------------------------------------------------------------
# echo를 여러 번 쓰면 여러 줄을 출력할 수 있다.
echo "Welcome to study-languages."
echo "This is Day 1 of Shell basics."


# ------------------------------------------------------------
# 3) 변수와 함께 사용
# ------------------------------------------------------------
# Shell에서는 변수에 값을 저장한 뒤 echo로 출력할 수 있다.
name="Junyeong"
echo "Hello, $name!"


# ------------------------------------------------------------
# 4) 현재 Shell 환경 정보 일부 출력 예시
# ------------------------------------------------------------
# USER: 현재 사용자 이름
# HOME: 홈 디렉토리 경로
echo "Current user: $USER"
echo "Home directory: $HOME"


# ------------------------------------------------------------
# 5) 구분선 출력
# ------------------------------------------------------------
# 실무에서는 로그를 보기 좋게 만들기 위해 구분선을 자주 출력한다.
echo "------------------------------"
echo "Echo practice completed."
echo "------------------------------"